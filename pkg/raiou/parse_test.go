func TestParsePkginfoString(t *testing.T) {
	const data = `# Generated by makepkg
pkgname = hello
pkgbase = hello
pkgver = 1.0.0-1
pkgdesc = A test package
url = https://example.com
arch = x86_64
size = 12345
builddate = 1700000000
license = MIT
depend = glibc
depend = gcc-libs
xdata = pkgtype=pkg
`

	p, err := raiou.ParsePkginfoString(data)
	if err != nil {
		t.Fatalf("ParsePkginfoString failed: %v", err)
	}

	if p.PkgName != "hello" {
		t.Errorf("PkgName = %q, want hello", p.PkgName)
	}
	if p.PkgVer != "1.0.0-1" {
		t.Errorf("PkgVer = %q, want 1.0.0-1", p.PkgVer)
	}
	if p.Arch != "x86_64" {
		t.Errorf("Arch = %q, want x86_64", p.Arch)
	}
	if p.Size != 12345 {
		t.Errorf("Size = %d, want 12345", p.Size)
	}
	if len(p.Depend) != 2 {
		t.Errorf("Depend len = %d, want 2", len(p.Depend))
	}
	if p.PkgType != "pkg" {
		t.Errorf("PkgType = %q, want pkg", p.PkgType)
	}
}

