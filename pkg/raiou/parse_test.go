func TestParseSrcinfoString(t *testing.T) {
	const data = "pkgbase = foo\n" +
		"\tpkgver = 1.2.3\n" +
		"\tpkgrel = 2\n" +
		"\tepoch = 1\n" +
		"\tarch = x86_64\n" +
		"\n" +
		"pkgname = foo\n" +
		"\n" +
		"pkgname = foo-libs\n"

	s, err := raiou.ParseSrcinfoString(data)
	if err != nil {
		t.Fatalf("ParseSrcinfoString failed: %v", err)
	}
	if s.PkgBase != "foo" {
		t.Errorf("PkgBase = %q, want foo", s.PkgBase)
	}
	if s.PkgVer != "1.2.3" {
		t.Errorf("PkgVer = %q, want 1.2.3", s.PkgVer)
	}
	if s.PkgRel != "2" {
		t.Errorf("PkgRel = %q, want 2", s.PkgRel)
	}
	if len(s.Packages) != 2 {
		t.Fatalf("Packages len = %d, want 2", len(s.Packages))
	}
	if s.Packages[0].PkgName != "foo" || s.Packages[1].PkgName != "foo-libs" {
		t.Errorf("Packages names = [%q %q], want [foo foo-libs]", s.Packages[0].PkgName, s.Packages[1].PkgName)
	}
}

